package main

import "os"

func main() {
    // Get the arguments passed in from the command line
    args := os.Args

    // Use the len function to count the number of arguments
    numArgs := len(args) - 1 // the first argument is always the program name

    // Print the number of arguments
    println("Number of arguments:", numArgs)
}
